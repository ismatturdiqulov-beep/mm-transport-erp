-- MM Transport ERP — миграция 3: исправление ограничения transport.type.
--
-- 0001_init.sql (по ТЗ) ограничивал transport.type значениями 'truck'|'trailer'|'other'.
-- При реальной загрузке данных выяснилось, что приложение хранит там русские названия
-- категорий транспорта: 'Тягач', 'Прицеп', 'Полуприцеп', 'Фургон', 'Легковой' — то есть
-- реальная бизнес-таксономия шире и на другом языке, чем предполагало ТЗ. Маппинг в 3
-- английских значения был бы с потерей смысла (Полуприцеп/Фургон/Легковой пришлось бы
-- искусственно сминать в одно "other"), поэтому просто снимаем ограничение и оставляем
-- реальные категории как свободный текст.
alter table transport drop constraint if exists transport_type_check;
