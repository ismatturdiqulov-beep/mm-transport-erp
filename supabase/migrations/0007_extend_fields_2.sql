-- MM Transport ERP — миграция 7: вторая, более тщательная сверка полей JS-приложения
-- со схемой (см. 0002_extend_fields.sql для первой сверки). Найдено агентом-исследователем
-- при подготовке клиентского кода загрузки/записи данных — до этой миграции эти поля
-- реально терялись бы при переносе в Postgres.
--
-- Намеренно НЕ добавлены (обоснование):
--  - transport.planned_to_days — грепом по всему файлу не нашлось ни одного места, которое
--    его пишет (читается один раз, но никогда не устанавливается) — мёртвое поле.
--  - tirs.returnedDriver — читается в 2 местах, но не пишется нигде — мёртвое поле
--    (вытеснено returnedOffice).
--  - trips.tirIds / trips.dozvIds — не добавляем как колонки. Это дублирующий кэш в JS;
--    правильный источник истины — обратная связь tirs.trip_id/dozv.trip_id (уже есть).
--    При загрузке на клиенте будем вычислять trip.tirIds = tirs.filter(t=>t.trip_id===...),
--    а не хранить отдельно (иначе поле будет рассинхронизироваться).

-- ============================================================
-- transport: срок аренды, резервное имя ответственного, реальный пробег (см. ниже),
-- статус ТО (пишется, хоть сейчас и не перечитывается — дешевле сохранить, чем терять),
-- плановый интервал ТО в км.
--
-- ВАЖНО про пробег: колонка transport.mileage из 0001 оказалась МЁРТВОЙ — в реальном
-- приложении нигде не читается и не пишется как t.mileage. Настоящий "текущий пробег"
-- хранится в t.currentMileage + t.mileageUpdatedAt (дата последнего ручного обновления),
-- и на экране показывается максимум из currentMileage / последнего км рейса / пробега на
-- последнем ТО. Добавляем current_mileage/mileage_updated_at; старую mileage не трогаем
-- (может пригодиться позже, вреда от неиспользуемой колонки нет).
-- ============================================================
alter table transport
  add column contract_end       date,
  add column resp                text,     -- имя ответственного текстом (резерв, если resp_kontragent_id не задан/устарел)
  add column current_mileage     numeric,  -- реальный "текущий пробег", см. пояснение выше
  add column mileage_updated_at  date,
  add column maintenance_status  text,     -- 'ok' | 'inservice' — пишется приложением, хоть сейчас и не перечитывается
  add column planned_to_mileage  numeric;  -- плановый интервал ТО (км)

-- ============================================================
-- fleet: заметка по единице ПС (то самое поле, о котором вы спрашивали изначально)
-- ============================================================
alter table fleet add column note text;

-- ============================================================
-- maintenance: категория ремонта, дублирующие/сводные поля по стоимости и валюте на
-- уровне записи (используются для рендера таблицы отдельно от items[])
-- ============================================================
alter table maintenance
  add column type         text,
  add column cost_uzs     numeric,
  add column currency     text,
  add column custom_rate  numeric,
  add column parts        text;

-- ============================================================
-- trip_expenses: страна (для расходов, связанных с пересечением границы) и исходная
-- сумма в валюте расхода до конвертации в сум (amount хранит уже сум)
-- ============================================================
alter table trip_expenses
  add column country      text,
  add column amount_orig  numeric;

-- ============================================================
-- trips: возврат наличных от водителя (неизрасходованный аванс) при закрытии рейса
-- ============================================================
alter table trips add column cash_return numeric;

-- ============================================================
-- kontragenty: отдельный финансовый гроссбух по контрагенту (saveKontragentOp) —
-- полностью отдельная сущность от fleet.ops, нигде раньше не учитывалась.
-- ============================================================
alter table kontragenty add column ops jsonb;

-- ============================================================
-- tirs: на какой единице ПС сейчас числится книжка (dozv.fleet_id уже был в 0001,
-- у tirs аналогичного поля не было — реальный пробел)
-- ============================================================
alter table tirs add column fleet_id uuid references fleet(id);
create index idx_tirs_fleet on tirs(fleet_id);

-- ============================================================
-- dozv: дата закрытия Е-ПЕРМИТа при завершении рейса (аналог returned_office для
-- бумажных дозволов, но для электронных)
-- ============================================================
alter table dozv add column closed date;

-- ============================================================
-- kassa: связь кассовой операции с рейсом (выдача аванса водителю) и с конкретным
-- расходом рейса (чтобы можно было найти и удалить кассовую запись при правке расхода)
-- ============================================================
alter table kassa
  add column trip_id      uuid references trips(id),
  add column trip_exp_id  uuid references trip_expenses(id);
create index idx_kassa_trip on kassa(trip_id);
create index idx_kassa_trip_exp on kassa(trip_exp_id);
