-- MM Transport ERP — миграция 17: kassa_wallets.id был первичным ключом САМ ПО СЕБЕ
-- (просто text, см. 0008) — то есть глобально уникальным на всю базу, а не в рамках
-- одной компании. Стандартные кошельки создаются с фиксированными id ('cash','card',
-- 'bank') для КАЖДОЙ компании — а раз id должен быть уникален глобально, вторая
-- компания физически не может завести собственный кошелёк 'cash', пока он уже занят
-- первой. Найдено 2026-07-15 при реальном тесте создания второй тестовой компании —
-- до реальных клиентов не дошло, но это был бы жёсткий блокер для любой второй
-- компании в системе.
--
-- Никаких внешних ключей на kassa_wallets.id нет (кошелёк везде хранится как обычный
-- текст в kassa.wallet/finance.wallet, см. 0008) — менять первичный ключ безопасно.
alter table kassa_wallets drop constraint kassa_wallets_pkey;
alter table kassa_wallets add primary key (company_id, id);
