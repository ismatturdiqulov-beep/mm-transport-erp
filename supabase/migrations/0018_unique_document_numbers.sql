-- MM Transport ERP — миграция 18: номер ТИР-карнета и дозвола должен быть уникальным
-- в рамках компании — пользователь явно об этом сказал при обсуждении пакетного приёма
-- нескольких ТИР за раз 2026-07-15. Раньше ничего не мешало случайно завести две записи
-- с одинаковым номером (опечатка при ручном вводе или повтор в вставленном списке).
-- Уникальность — в рамках company_id, не глобально: это те же соображения, что и для
-- kassa_wallets в 0017, просто здесь коллизия менее вероятна (номера выдаёт АСМАП/Минтранс
-- централизованно), но лучше не полагаться на удачу в мультитенантной системе.
create unique index tirs_company_num_uniq on tirs(company_id, num);
create unique index dozv_company_num_uniq on dozv(company_id, num);
