-- MM Transport ERP — миграция 19: связать kassa с maintenance
-- Найдено аудитом 2026-07-16: сохранение записи ТО (maintenance) может создать расход
-- в кассе (если указана сумма и кошелёк), но раньше между ними не было никакой связи —
-- при удалении записи ТО расход в кассе оставался висеть навсегда, молча завышая
-- фактические траты компании.
--
-- В отличие от kassa.trip_id/trip_exp_id (0010, ON DELETE SET NULL — деньги по рейсу
-- реально были потрачены, история должна остаться, даже если сам рейс удалили), здесь
-- логика другая: запись ТО удаляют почти всегда именно потому, что её ввели по ошибке
-- (не туда, задвоили, опечатались в сумме) — то есть денег на самом деле не уходило,
-- и вместе с записью ТО должен уйти и её расход. Поэтому здесь ON DELETE CASCADE, а не
-- SET NULL.
alter table kassa add column maintenance_id uuid references maintenance(id) on delete cascade;
