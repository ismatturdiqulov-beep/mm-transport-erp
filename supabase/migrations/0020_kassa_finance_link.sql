-- MM Transport ERP — миграция 20: связать kassa с finance для "Приход" от арендатора
-- Найдено аудитом 2026-07-16: savePayment (кнопка "Приход") создаёт ОДНОВРЕМЕННО запись
-- в finance (paid=amount, закрывает долг арендатора) И запись в kassa (реальные деньги в
-- кассе). Между ними не было связи — при удалении записи из кассы долг у арендатора
-- оставался закрытым, хотя денег в кассе больше нет.
--
-- ON DELETE CASCADE (не SET NULL, как у trip_id/trip_exp_id в 0010): если удаляют запись
-- прихода — это почти всегда отмена ошибочно введённой оплаты, а не архивация реальной
-- истории, поэтому обе половины (касса + начисление) должны исчезать вместе.
alter table kassa add column finance_id uuid references finance(id) on delete cascade;
