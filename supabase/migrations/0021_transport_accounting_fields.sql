-- MM Transport ERP — миграция 21: поля для отчётов бухгалтеру (2026-07-17)
--
-- Контекст: бухгалтер запрашивает два разных отчёта по транспорту —
-- (1) по арендованному транспорту для НДФЛ (компания — налоговый агент, платит НДФЛ
--     за арендодателей) и (2) по собственному транспорту для расчёта амортизации.
--
-- ВАЖНО — не путать с fleet.plan: fleet.plan — это абонентская плата, которую
-- арендатор платит компании за пользование её услугами (тарифицируется на единицу
-- Подвижного состава). contract_price здесь — совершенно другая величина: формальная
-- договорная цена аренды САМОЙ МАШИНЫ, нужна исключительно для налоговой базы по
-- НДФЛ. Пользователь явно подчеркнул не путать эти два поля.
--
-- Договор аренды (номер/цена/даты) — по факту на уровне каждой единицы транспорта,
-- а не на уровне контрагента: один арендодатель нередко сдаёт несколько машин, и у
-- каждой — свой номер договора и своя цена (подтверждено реальным образцом файла
-- бухгалтера: у одного арендодателя тягач и прицеп могут идти по разным ценам).
-- transport.contract_start/contract_end уже были добавлены в 0002/0007 на уровне
-- машины — этого шаблона просто не хватало для номера и цены.
alter table transport
  add column contract_num    text,
  add column contract_price  numeric,
  add column contract_status text not null default 'active', -- 'active'|'free'|'terminated'|'sold' (Действует/Текин/Расторгнут/Продан)
  add column purchase_price  numeric,  -- балансовая/покупная стоимость — для амортизации (только собственный транспорт, owner='ММ')
  add column balance_date    date;     -- дата постановки на баланс — для амортизации
