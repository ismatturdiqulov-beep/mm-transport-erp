-- MM Transport ERP — миграция 22: отметка "допущен к TIR / включать в отчёт АСМАП"
-- (2026-07-17). АСМАП периодически запрашивает список подвижного состава — но не
-- весь транспорт компании реально допущен к международным перевозкам с книжкой МДП,
-- поэтому нужен ручной, стабильный признак на карточке транспорта, а не автоматический
-- отбор по типу/статусу. По умолчанию false — в отчёт попадает только то, что явно
-- отметили, а не всё подряд.
alter table transport add column asmap_include boolean not null default false;
