-- MM Transport ERP — миграция 32: realtime для списка сообщений от водителей/контрагентов
-- (2026-07-20). Раньше driver_messages грузился только один раз при входе в приложение
-- (loadAllData на логине) — если у диспетчера уже была открыта страница, новое сообщение
-- появлялось только после ручного обновления (F5). Включаем Postgres Changes для этой
-- таблицы, чтобы новое сообщение и счётчик в меню обновлялись мгновенно. RLS-политики
-- таблицы (0025/0029) продолжают применяться и к realtime-трафику — чужая компания
-- событие не получит.
alter publication supabase_realtime add table driver_messages;
