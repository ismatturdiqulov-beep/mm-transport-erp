-- MM Transport ERP — миграция 33: новый тип контрагента "Наёмный водитель"
-- (2026-07-21). Раньше в системе не было способа отличить владельца/бенефициара
-- ПС (тариф "Водитель ММ" = внутренний, переименован на уровне интерфейса в
-- "Собственник") от человека, который физически водит машину, но не участвует
-- в финансах. У 3 учредителей компании есть свои машины (тариф "Собственник"),
-- но за рулём — нанятые водители, которых надо подключать к боту (напоминания по
-- личным документам + связь с диспетчером), не давая им доступа к финансам и не
-- позволяя назначать их "ответственным лицом" за ПС (иначе начисления за ТИР/
-- Дозвол будут уходить в никуда) — решение пользователя 2026-07-21.
alter table kontragenty drop constraint kontragenty_type_check;
alter table kontragenty add constraint kontragenty_type_check
  check (type in ('tenant','driver_mm','client','service','other','hired_driver'));
